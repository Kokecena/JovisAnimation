    private void animation(int direction) {
        try {
            switch (direction) {
                case 1:
                    Move.Right(this, 0, this.getX() + this.getWidth(),
                            Integer.parseInt(this.jTextField2.getText()), Integer.parseInt(this.jTextField1.getText()));
                    break;
                case 2:
                    Move.Left(this, 0, this.getX() - this.getWidth(),
                            Integer.parseInt(this.jTextField2.getText()), Integer.parseInt(this.jTextField1.getText()));
                    break;
                case 3:
                    Move.Up(this, 0, this.getY() - this.getHeight(),
                            Integer.parseInt(this.jTextField2.getText()), Integer.parseInt(this.jTextField1.getText()));
                    break;
                case 4:
                    Move.Down(this, 0, this.getY() + this.getHeight(),
                            Integer.parseInt(this.jTextField2.getText()), Integer.parseInt(this.jTextField1.getText()));
                    break;
            }
            if (this.jCheckBox1.isSelected()) {
                Fade.fadeOut(this, 30, 0.1f);
            }
        } catch (Exception ex) {
            Move.shake(this, 5);
        }
    }

