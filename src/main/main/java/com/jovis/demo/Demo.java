    private void initPulsatingAnimation(int speed, PulsatingBorder b) {
        Timer t = new Timer(speed, null);
        t.setRepeats(true);
        t.addActionListener(new ActionListener() {
            float opacity = 0.0f;
            boolean reverse = false;

            @Override
            public void actionPerformed(ActionEvent e) {
                if (isVisible()) {
                    opacity += reverse ? -0.1f : 0.1f;
                    b.setThickness(reverse ? Math.max(opacity, 0f) : Math.min(opacity, 1f));
                    if (!reverse && opacity >= 1f) {
                        reverse = true;
                    } else if (opacity <= 0f) {
                        reverse = false;
                    }
                } else {
                    t.stop();
                }
            }
        });
        t.start();
    }

